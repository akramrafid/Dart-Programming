void main() {
  
  var name = 'Akram Rafid'; // Inferred as String
  final int age = 20; // Immutable, but can be set at runtime
  const double height = 1.75; // Immutable and must be set at compile-time
  bool isStudent = true; // Mutable variable

  print('Name: $name');
  print('Age: $age');
  print('Height: $height');
  print('Is Student: $isStudent');
  

}